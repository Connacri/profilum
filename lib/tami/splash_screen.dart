class _SplashScreenState extends State<SplashScreen>
    with SingleTickerProviderStateMixin {
  late AnimationController _controller;
  late Animation<double> _fadeAnimation;
  late Animation<double> _scaleAnimation;

  @override
  void initState() {
    super.initState();

    // Animations
    _controller = AnimationController(
      duration: const Duration(milliseconds: 1500),
      vsync: this,
    );

    _fadeAnimation = Tween<double>(begin: 0.0, end: 1.0).animate(
      CurvedAnimation(
        parent: _controller,
        curve: const Interval(0.0, 0.5, curve: Curves.easeIn),
      ),
    );

    _scaleAnimation = Tween<double>(begin: 0.8, end: 1.0).animate(
      CurvedAnimation(
        parent: _controller,
        curve: const Interval(0.0, 0.5, curve: Curves.easeOut),
      ),
    );

    _controller.forward();

    // DÃ©marrer logique de routing
    _initializeApp();
  }

  @override
  void dispose() {
    _controller.dispose();
    super.dispose();
  }

  /// â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•
  /// ğŸ” LOGIQUE D'INITIALISATION
  /// â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•
  Future<void> _initializeApp() async {
    await Future.delayed(const Duration(milliseconds: 2000));

    if (!mounted) return;

    // â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•
    // 1. DÃ‰TECTION ROUTE ADMIN
    // â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•
    final currentRoute = ModalRoute.of(context)?.settings.name;
    final isAdminRoute = AppRoutes.isAdminRoute(currentRoute);

    if (isAdminRoute) {
      await _handleAdminRouting();
      return;
    }

    // â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•
    // 2. ROUTING USER NORMAL
    // â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•
    await _handleUserRouting();
  }

  /// â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•
  /// ğŸ‘” ROUTING ADMIN
  /// â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•
  Future<void> _handleAdminRouting() async {
    final adminProvider = context.read<AdminAuthProvider>();

    // VÃ©rifier si admin dÃ©jÃ  authentifiÃ©
    await adminProvider.checkAuthStatus();

    if (!mounted) return;

    if (adminProvider.isAuthenticated) {
      // DÃ©jÃ  connectÃ© â†’ Dashboard
      Navigator.pushReplacementNamed(context, AppRoutes.adminDashboard);
    } else {
      // Pas connectÃ© â†’ Login
      Navigator.pushReplacementNamed(context, AppRoutes.adminLogin);
    }
  }

  /// â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•
  /// ğŸ‘¤ ROUTING USER NORMAL
  /// â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•â•
  Future<void> _handleUserRouting() async {
    final authProvider = context.read<AuthProvider>();
    final guestProvider = context.read<GuestModeProvider>();

    // Charger documents guest
    await guestProvider.loadGuestDocuments();

    if (!mounted) return;

    // VÃ©rifier session utilisateur
    // Note: _initAuth() est dÃ©jÃ  appelÃ© dans AuthProvider constructor
    // On attend juste que le status soit dÃ©terminÃ©

    await Future.delayed(const Duration(milliseconds: 500));

    if (!mounted) return;

    // DÃ©cision de routing selon AuthStatus
    switch (authProvider.status) {
      case AuthStatus.authenticated:
        // Utilisateur connectÃ© avec profil complet
        Navigator.pushReplacementNamed(context, AppRoutes.userHome);
        break;

      case AuthStatus.profileIncomplete:
        // Utilisateur connectÃ© mais profil incomplet
        // Note: La logique de ProfileCompletionScreen existe dans main.dart
        // On redirige vers home qui gÃ©rera l'affichage du prompt
        Navigator.pushReplacementNamed(context, AppRoutes.userHome);
        break;

      case AuthStatus.emailVerificationPending:
        // Email non vÃ©rifiÃ©
        Navigator.pushReplacementNamed(context, AppRoutes.emailVerification);
        break;

      case AuthStatus.unauthenticated:
      case AuthStatus.initial:
      case AuthStatus.error:
      default:
        // Pas de session â†’ Mode Guest par dÃ©faut
        Navigator.pushReplacementNamed(context, AppRoutes.welcome);
        break;
    }
  }

